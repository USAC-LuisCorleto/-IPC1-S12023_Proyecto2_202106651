    //Abrir
    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try {
            FileInputStream abrir = new FileInputStream("BaseDeDatos.ser");
            ObjectInputStream escribir = new ObjectInputStream(abrir);
            BaseDeDatos = (ListaSimple) escribir.readObject();
            escribir.close();
            abrir.close();
        } catch (IOException i) {
            BaseDeDatos = new ListaSimple();

        } catch (ClassNotFoundException c) {
            BaseDeDatos = new ListaSimple();
        }
    }//GEN-LAST:event_formWindowOpened

    //Cerrar
    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        try {
            FileOutputStream guardar = new FileOutputStream("BaseDeDatos.ser");
            ObjectOutputStream escribir = new ObjectOutputStream(guardar);
            escribir.writeObject(BaseDeDatos);
            escribir.close();
            guardar.close();
        } catch (IOException i) {
            i.printStackTrace();
        }
    }//GEN-LAST:event_formWindowClosing

