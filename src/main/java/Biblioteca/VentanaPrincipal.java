    private void irBibliotecaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_irBibliotecaActionPerformed
        if (nombreUsuario.getText() == null || nombreUsuario.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese al menos un carácter.", "Error", JOptionPane.WARNING_MESSAGE);
        } else {
            nombreUsuarioIngresado = nombreUsuario.getText();
            ContenedorCategorías = (Nodo) BaseDeDatos.find(nombreUsuarioIngresado);
            contenedorCategoríasLista = (ListaDobleE) ContenedorCategorías.Contenido;
            i = contenedorCategoríasLista.getSize();
            nombresCategorías = new String[i];

            nombreFotos = new String[j];

            int j = 0;
            Nodo aux1 = contenedorCategoríasLista.cabecera;
            while (j < contenedorCategoríasLista.tamaño) {
                nombresCategorías[j] = aux1.Contenido.nombre;
                aux1 = aux1.getSiguiente();
                j++;
            }

            Biblioteca B = new Biblioteca();
            B.setVisible(true);
            B.setLocationRelativeTo(null);
        }
    }//GEN-LAST:event_irBibliotecaActionPerformed

